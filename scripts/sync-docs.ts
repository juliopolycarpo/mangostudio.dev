import { execFile } from 'node:child_process';
import { mkdir, readdir, readFile, rm, writeFile } from 'node:fs/promises';
import { dirname, join, posix, relative, resolve, sep } from 'node:path';
import { promisify } from 'node:util';

import type { Lang } from '../src/i18n/types';

const execFileAsync = promisify(execFile);

const DEFAULT_SOURCE_DIR = join(process.cwd(), '..', 'mangostudio');
const GENERATED_CONTENT_DIR = join('src', 'content', 'docs');
const GENERATED_MANIFEST_PATH = join('src', 'data', 'docs.generated.ts');
const SOURCE_REPO = 'https://github.com/juliopolycarpo/mangostudio';
const AUTO_GENERATED_HEADER =
  '// AUTO-GENERATED by scripts/sync-docs.ts - edit the generator, not this file.';

type DocGroupId =
  | 'getting-started'
  | 'guides'
  | 'features'
  | 'providers'
  | 'architecture'
  | 'reference'
  | 'operations';

interface SourceDocDefinition {
  slug: string;
  groupId: DocGroupId;
  order: number;
  sidebarLabel?: Partial<Record<Lang, string>>;
  sources: Record<Lang, string>;
}

interface GeneratedDocItem {
  slug: string;
  title: string;
  sidebarLabel: string;
  sourcePath: string;
  sourceUrl: string;
  groupId: DocGroupId;
  groupTitle: string;
  order: number;
}

interface GeneratedDocGroup {
  id: DocGroupId;
  title: string;
  items: GeneratedDocItem[];
}

export interface SyncDocsOptions {
  sourceDir?: string;
  repoRoot?: string;
  check?: boolean;
  sourceCommit?: string;
}

export interface SyncDocsResult {
  sourceCommit: string;
  docs: Record<Lang, GeneratedDocGroup[]>;
}

